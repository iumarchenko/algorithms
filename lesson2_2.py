# 9. Среди натуральных чисел, которые были введены, найти наибольшее по сумме цифр. Вывести на экран это число и сумму его цифр.
max = 0
numb = 0

def num_sum(a):
    c = 0
    for i in a:
        c += int(i)
    return c

while True:
    a = input("Введите число: ")
    if a == "0":
        break
    b = num_sum(a)
    if b > max:
        max = b
        numb = a

    print("На текущей итерации число с наибольшей суммой чисел({}) {}".format(max, numb))