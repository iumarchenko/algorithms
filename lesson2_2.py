# 2. Посчитать четные и нечетные цифры введенного натурального числа. Например, если введено число 34560, то у него 3 четные цифры (4, 6 и 0) и 2 нечетные (3 и 5).

while True:
    numb = input("Введите число: ")
    if numb.isdigit():
        break
    else:
        print("Число введено некорректно, введите еще раз")

even = 0
n_even = 0

for i in numb:
    if int(i) % 2 == 0:
        even += 1
    else:
        n_even += 1

print("Количество четных чисел {}".format(even))
print("Количество нечетных чисел {}".format(n_even))

